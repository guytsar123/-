// בונה אינדקס מנורמל: מפתח-מנורמל → key קטגוריה.
const SEED_INDEX = (() => {
  const idx = {};
  for (const [cat, names] of Object.entries(SEED)) {
    for (const name of names) {
      idx[normalizeKey(name)] = cat;
    }
  }
  return idx;
})();

// סיווג פריט בודד ממקורות מקומיים בלבד (זרעי + נלמד). מחזיר key או null.
export function classifyLocal(name) {
  const key = normalizeKey(name);
  if (!key) return 'other';
  if (SEED_INDEX[key]) return SEED_INDEX[key];

  const learned = loadLearnedDict();
  if (learned[key]) return learned[key];

  // ניסיון התאמה חלקית: אם מילה כלשהי בפריט מוכרת (למשל "חלב סויה" → מחלבה דרך "חלב").
  const words = key.split(' ');
  for (const w of words) {
    if (SEED_INDEX[w]) return SEED_INDEX[w];
    if (learned[w]) return learned[w];
  }
  return null; // לא ידוע — ייתכן שיסווג ע"י LLM
}

// שמירת סיווג שנלמד (מ-LLM או מהמשתמש) חזרה למילון.
export function rememberCategory(name, categoryKey) {
  if (!VALID_KEYS.has(categoryKey)) categoryKey = 'other';
  const key = normalizeKey(name);
  if (!key) return;
  const learned = loadLearnedDict();
  learned[key] = categoryKey;
  saveLearnedDict(learned);
}

export function isValidCategory(key) {
  return VALID_KEYS.has(key);
}
